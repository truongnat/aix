def _require_init() -> None:
    """Exit with a friendly message if .goal/config.yaml does not exist."""
    if not PROJECT_CONFIG_PATH.exists():
        rprint(
            Panel(
                "[bold yellow]Project not initialised.[/bold yellow]\n\n"
                "Run [bold cyan]goal init[/bold cyan] first to set up config for this directory.",
                title="⚠️  Not initialised",
                border_style="yellow",
            )
        )
        raise typer.Exit(1)


def _close_checkpointer(checkpointer: Any) -> None:
    """Close the SqliteSaver's underlying connection if possible."""
    conn = getattr(checkpointer, "conn", None)
    if conn is not None:
        with contextlib.suppress(Exception):
            conn.close()


def _write_and_open(file_path: Path, content: str) -> str:
    """Write content to file_path, print path for user to edit, wait for Enter."""
    file_path.write_text(content, encoding="utf-8")
    rprint(f"\n[bold]Open and edit:[/bold] [cyan]{file_path.resolve()}[/cyan]")
    rprint("[dim]Save the file in your editor, then press Enter to continue...[/dim]")
    input()
    return file_path.read_text(encoding="utf-8")


def _ask_action(label: str, has_skip: bool = False) -> str:
    """Show numbered menu and return action string: approve | regenerate | skip | abort."""
    options = ["1) approve", "2) regenerate", "3) abort"]
    if has_skip:
        options = ["1) approve", "2) regenerate", "3) skip", "4) abort"]
    rprint(f"\n[bold]{label}[/bold]  " + "  ".join(f"[cyan]{o}[/cyan]" for o in options))
    mapping = {"1": "approve", "2": "regenerate", "3": "skip" if has_skip else "abort", "4": "abort"}
    while True:
        raw = input("Choice [1]: ").strip() or "1"
        if raw in mapping:
            return mapping[raw]
        rprint(f"[yellow]Enter {'/'.join(mapping.keys())}[/yellow]")


def _run_with_approvals(
    graph: Any, state: Any, config: dict[str, Any], goal_dir: Path
) -> dict[str, Any]:
    """Run the graph through plan / rules / tasks interrupts.

    At each pause we save the artifact as a file in goal_dir, open it in
    the editor, then ask user to approve or abort.
    """
    current = state

    while True:
        final_state = graph.invoke(current, config)

        snapshot = graph.get_state(config)
        next_nodes = tuple(snapshot.next or ())

        if not next_nodes:
            return final_state  # Pipeline finished

        next_node = next_nodes[0]

        # ---------- PLAN approval ----------
        if next_node == "plan_approval":
            plan_text = final_state.get("plan", "")
            plan_file = goal_dir / "plan.md"
            rprint(f"[dim]Saving plan to:[/dim] [cyan]{plan_file}[/cyan]")
            updated = _write_and_open(plan_file, plan_text or "")
            updated = updated.strip()
            if updated and updated != plan_text.strip():
                graph.update_state(config, {"plan": updated}, as_node="plan")
            choice = _ask_action("Plan")
            if choice == "abort":
                raise typer.Exit(1)
            if choice == "regenerate":
                fb = Prompt.ask("Feedback (what to change)")
                graph.update_state(config, {"plan": "", "feedback": fb}, as_node="__start__")
                current = None
                continue
            # approve -> reset feedback and resume
            graph.update_state(config, {"feedback": None}, as_node="plan")

        # ---------- RULES approval ----------
        elif next_node == "rules_approval":
            questions = final_state.get("rules", "")
            rules_file = goal_dir / "rules.md"
            rprint(f"[dim]Saving rules to:[/dim] [cyan]{rules_file}[/cyan]")
            updated = _write_and_open(rules_file, questions or "")
            updated = updated.strip()
            if updated:
                graph.update_state(config, {"rules": updated}, as_node="rules")
            choice = _ask_action("Rules", has_skip=True)
            if choice == "abort":
                raise typer.Exit(1)
            if choice == "regenerate":
                fb = Prompt.ask("Feedback (what to change)")
                graph.update_state(config, {"rules": "", "feedback": fb}, as_node="plan")
                current = None
                continue
            # approve / skip -> reset feedback and resume
            graph.update_state(config, {"feedback": None}, as_node="rules")

        # ---------- TASKS approval ----------
        elif next_node == "tasks_approval":
            from agentic_goal.nodes import _parse_tasks_to_kanban

            tasks_list = final_state.get("tasks") or []
            tasks_md = tasks_list[0].get("raw", "") if tasks_list else ""
            kanban = final_state.get("kanban", {})
            tasks_file = goal_dir / "tasks.md"
            rprint(
                f"[dim]Saving tasks ({len(kanban)} tickets) to:[/dim] [cyan]{tasks_file}[/cyan]"
            )
            updated = _write_and_open(tasks_file, tasks_md or "")
            updated = updated.strip()
            if updated and updated != tasks_md.strip():
                new_kanban = _parse_tasks_to_kanban(updated)
                graph.update_state(
                    config,
                    {"tasks": [{"raw": updated}], "kanban": new_kanban},
                    as_node="tasks",
                )
            choice = _ask_action("Tasks")
            if choice == "abort":
                raise typer.Exit(1)
            if choice == "regenerate":
                fb = Prompt.ask("Feedback (what to change)")
                graph.update_state(
                    config,
                    {"tasks": [], "kanban": {}, "feedback": fb},
                    as_node="rules",
                )
                current = None
                continue
            # approve -> reset feedback and resume
            graph.update_state(config, {"feedback": None}, as_node="tasks")

        else:
            # Unknown interrupt — just resume
            rprint(f"[yellow]Pausing at unknown node: {next_node}; resuming.[/yellow]")

        # Resume by passing None
        current = None


@app.command()
def init(
    force: bool = typer.Option(False, "--force", "-f", help="Overwrite existing config"),
) -> None:
    """Initialise a new agentic-goal project in the current directory."""
    config_path = PROJECT_CONFIG_PATH
    if config_path.exists() and not force:
        rprint(
            Panel(
                f"[yellow]Config already exists:[/yellow] {config_path}\n\n"
                "Use [bold]goal init --force[/bold] to overwrite.",
                title="Already initialised",
                border_style="yellow",
            )
        )
        raise typer.Exit(0)

    config_path.parent.mkdir(parents=True, exist_ok=True)
    config_path.write_text(_CONFIG_TEMPLATE, encoding="utf-8")

    skills_dir = config_path.parent / "skills"
    skills_dir.mkdir(exist_ok=True)
    global_skill = skills_dir / "global.md"
    if not global_skill.exists():
        global_skill.write_text(
            "# Global project context\n\n"
            "<!-- Add conventions, domain knowledge, or constraints that apply to ALL agents. -->\n"
            "<!-- Examples: target language version, prohibited libraries, naming conventions. -->\n",
            encoding="utf-8",
        )

    rprint(
        Panel(
            f"[bold green]Initialised![/bold green]\n\n"
            f"Config:  [cyan]{config_path}[/cyan]\n"
            f"Skills:  [cyan]{skills_dir}/[/cyan]\n\n"
            "Next steps:\n"
            "  1. Edit [cyan].goal/config.yaml[/cyan] to set your provider & models\n"
            "  2. Export your API key, e.g.:\n"
            "     [dim]export OPENROUTER_API_KEY=sk-or-...[/dim]\n"
            "  3. (Optional) add skill files to [cyan].goal/skills/[/cyan]\n"
            "  4. Run [bold cyan]goal start \"your idea\"[/bold cyan]",
            title="✅  goal init",
            border_style="green",
        )
    )


@app.command()
def start(
    idea: str = typer.Argument(..., help="Goal idea / requirement"),
    verbose: int = typer.Option(0, "--verbose", "-v", help="Verbosity (-v, -vv, -vvv)"),
    model_override: list[str] | None = typer.Option(  # noqa: B008
        None,
        "--model-override",
        help="Override model per role, e.g. coder=gpt-4",
    ),
    no_color: bool = typer.Option(False, "--no-color"),
    json_output: bool = typer.Option(False, "--json"),
) -> None:
    """Start a new goal pipeline from an idea."""
    _require_init()
    cfg = load_config()

    # Convert None to empty list
    if model_override is None:
        model_override = []

    # Apply model overrides from CLI
    for override in model_override:
        if "=" in override:
            role, model = override.split("=", 1)
            if role in cfg.roles:
                cfg.roles[role] = cfg.roles[role].model_copy(update={"model": model})

    validate_config(cfg, strict=False)

    goal_id = f"g_{uuid.uuid4().hex[:12]}"
    goal_dir = Path(".goal") / goal_id
    goal_dir.mkdir(parents=True, exist_ok=True)

    rprint(f"[bold green]Goal:[/bold green] {idea}")
    rprint(f"[dim]Goal ID:[/dim] {goal_id}")
    rprint(f"[dim]Workspace:[/dim] {goal_dir}")

    # Set up event bus with 3 sinks
    event_bus = EventBus(goal_id)
    event_bus.add_sink(TerminalSink(verbose=verbose))
    event_bus.add_sink(JsonlSink(goal_dir / "events.jsonl"))
    event_bus.add_sink(MarkdownSink(goal_dir / "transcript.md"))

    # Set event_bus in context for nodes to access
    set_event_bus(event_bus)

    # Build graph with checkpointer
    checkpointer = get_checkpointer(goal_dir)
    try:
        graph = build_graph(checkpointer=checkpointer)

        # Initial state
        initial_state: dict[str, Any] = {
            "goal_id": goal_id,
            "idea": idea,
            "phase": "planning",
            "messages": [],
            "plan": "",
            "rules": "",
            "tasks": [],
            "current_ticket_id": None,
            "kanban": {},
            "config_override": {},
            "cumulative_cost_usd": 0.0,
            "cumulative_tokens": 0,
            "interrupt_reason": None,
            "feedback": None,
        }

        # Run graph through plan -> rules -> tasks (with human approvals)
        config: dict[str, Any] = {
            "configurable": {"thread_id": goal_id},
            "recursion_limit": 200,
        }
        try:
            final_state = _run_with_approvals(graph, initial_state, config, goal_dir)
        except Exception as exc:
            _friendly_error(exc)
            raise typer.Exit(1) from None
    finally:
        _close_checkpointer(checkpointer)
        event_bus.close()

    rprint("[bold green]✓[/bold green] Plan, rules, and tasks generated.")
    rprint(f"[dim]Artifacts saved to:[/dim] {goal_dir}/")
    raise typer.Exit(0)


@app.command("continue")
def continue_cmd(
    goal_id: str = typer.Option("", "--id", help="Goal ID to resume (default: latest)"),
    verbose: int = typer.Option(0, "--verbose", "-v", help="Verbosity (-v, -vv, -vvv)"),
) -> None:
    """Resume the most recent (or specified) goal from checkpoint."""
    _require_init()
    cfg = load_config()

    validate_config(cfg, strict=False)

    # Find goal directory
    goal_dir: Path
    if goal_id:
        goal_dir = Path(".goal") / goal_id
        if not goal_dir.exists():
            rprint(f"[bold red]Goal not found:[/bold red] {goal_id}")
            raise typer.Exit(1)
    else:
        # Find latest goal
        goals_dir = Path(".goal")
        if not goals_dir.exists():
            rprint("[bold red]No goals found.[/bold red]")
            raise typer.Exit(1)
        goal_dirs = sorted(
            (p for p in goals_dir.iterdir() if p.is_dir() and p.name.startswith("g_")),
            key=lambda p: p.stat().st_mtime,
            reverse=True,
        )
        if not goal_dirs:
            rprint("[bold red]No goals found.[/bold red]")
            raise typer.Exit(1)
        goal_dir = goal_dirs[0]
        goal_id = goal_dir.name

    rprint(f"[bold yellow]Resuming goal:[/bold yellow] {goal_id}")
    rprint(f"[dim]Workspace:[/dim] {goal_dir}")

    # Set up event bus with 3 sinks
    event_bus = EventBus(goal_id)
    event_bus.add_sink(TerminalSink(verbose=verbose))
    event_bus.add_sink(JsonlSink(goal_dir / "events.jsonl"))
    event_bus.add_sink(MarkdownSink(goal_dir / "transcript.md"))

    # Set event_bus in context for nodes to access
    set_event_bus(event_bus)

    # Build graph with checkpointer
    checkpointer = get_checkpointer(goal_dir)
    try:
        graph = build_graph(checkpointer=checkpointer)

        # Load checkpointed state
        config: dict[str, Any] = {
            "configurable": {"thread_id": goal_id},
            "recursion_limit": 200,
        }
        checkpoint = checkpointer.get(config)
        if not checkpoint:
            rprint("[bold red]No checkpoint found for this goal.[/bold red]")
            raise typer.Exit(1)

        # Continue execution; pass None to let LangGraph resume from checkpoint
        # and let our helper handle any pending approval interrupts.
        try:
            final_state = _run_with_approvals(graph, None, config, goal_dir)
        except Exception as exc:
            _friendly_error(exc)
            raise typer.Exit(1) from None
    finally:
        _close_checkpointer(checkpointer)
        event_bus.close()

    rprint("[bold green]✓[/bold green] Goal execution continued.")
    rprint(f"[dim]Artifacts saved to:[/dim] {goal_dir}/")
    raise typer.Exit(0)


