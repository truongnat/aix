def _friendly_error(exc: Exception) -> None:
    """Print a concise, human-readable error panel for common API failures."""
    msg = str(exc)
    if "credit balance is too low" in msg or "insufficient_quota" in msg:
        rprint(
            Panel(
                "[bold red]Insufficient credits[/bold red]\n\n"
                "Your API account has run out of credits.\n"
                "→ Anthropic: https://console.anthropic.com/settings/billing\n"
                "→ OpenAI:    https://platform.openai.com/settings/organization/billing",
                title="💳 Payment Required",
                border_style="red",
            )
        )
    elif "invalid x-api-key" in msg or "invalid_api_key" in msg or "authentication" in msg.lower():
        rprint(
            Panel(
                "[bold red]Invalid API key[/bold red]\n\n"
                "Check that your API key is correct and exported:\n"
                "  export ANTHROPIC_API_KEY=sk-ant-...\n"
                "  export OPENAI_API_KEY=sk-...",
                title="🔑 Authentication Error",
                border_style="red",
            )
        )
    elif "not_found_error" in msg or "model:" in msg:
        rprint(
            Panel(
                f"[bold red]Model not found[/bold red]\n\n{msg}\n\n"
                "Check your model name in ~/.config/agentic-goal/config.yaml",
                title="🤖 Unknown Model",
                border_style="red",
            )
        )
    elif "rate_limit" in msg or "rate limit" in msg.lower() or "429" in msg:
        rprint(
            Panel(
                "[bold yellow]Rate limit hit[/bold yellow]\n\n"
                "Too many requests. Wait a moment then try again.",
                title="⏳ Rate Limit",
                border_style="yellow",
            )
        )
    elif "Connection" in msg or "connect" in msg.lower() or "timeout" in msg.lower():
        rprint(
            Panel(
                "[bold red]Connection error[/bold red]\n\n"
                f"{msg}\n\nCheck your internet connection and try again.",
                title="🌐 Network Error",
                border_style="red",
            )
        )
    else:
        rprint(
            Panel(
                f"[bold red]{type(exc).__name__}[/bold red]\n\n{msg}",
                title="❌ Error",
                border_style="red",
            )
        )

